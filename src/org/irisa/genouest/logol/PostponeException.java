package org.irisa.genouest.logol;

public class PostponeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7549534633416156721L;

	public PostponeException() {
		
	}

	public PostponeException(String arg0) {
		super(arg0);
		
	}

	public PostponeException(Throwable arg0) {
		super(arg0);
		
	}

	public PostponeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
